        public static List<T> ConvertDataTableToClass<T>(DataTable table)
        {
            if (table == null)
            {
                return new List<T>();
            }
            List<Dictionary<string, object>> deserializeObject = new List<Dictionary<string, object>>();
            Dictionary<string, object> childRow;
            foreach (DataRow row in table.Rows)
            {
                childRow = new Dictionary<string, object>();
                foreach (DataColumn col in table.Columns)
                {
                    var addItem = row[col];
                    if (addItem == DBNull.Value)
                        addItem = null;
                    childRow.Add(col.ColumnName, addItem);
                }
                deserializeObject.Add(childRow);
            }
            return DeserializeObject<List<T>>(SerializeObject(deserializeObject));
        }
        private static string SerializeObject(object value)
        {
            return JsonConvert.SerializeObject(value);
        }
        private static T DeserializeObject<T>(string value)
        {
            var jSetting = new JsonSerializerSettings { NullValueHandling = NullValueHandling.Ignore };
            return JsonConvert.DeserializeObject<T>(value, jSetting);
        }
